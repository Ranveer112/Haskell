-------------------------------------------------------------------------- 
--	Reversing a list.						--
-------------------------------------------------------------------------- 

rev :: [t] -> [t]

rev []    = []
rev (a:x) = rev x ++ [a]

--------------------------------------------------------------------------
--	 The rep function						--
--------------------------------------------------------------------------

rep :: Int -> t -> [t]

rep 0 ch = []
rep n ch = ch : rep (n-1) ch

-------------------------------------------------------------------------- 
--	Shifting elements about.					--
-------------------------------------------------------------------------- 

shift :: ((t,u),v) -> (t,(u,v))

shift ((a,b),c) = (a,(b,c))

