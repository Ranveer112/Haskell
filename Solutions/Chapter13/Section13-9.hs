-------------------------------------------------------------------------- 
--	The factorial function.					--
-------------------------------------------------------------------------- 

fac :: Int -> Int

fac 0 = 1
fac n = n * fac (n-1)

facMap = map fac [0 .. ]

-------------------------------------------------------------------------- 
--	A process-based factorial list.					--
-------------------------------------------------------------------------- 

facs :: [Int]

facs = 1 : zipWith (*) [1 .. ] facs


