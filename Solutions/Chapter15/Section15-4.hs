-------------------------------------------------------------------------- 
--	Examples -- saving values in where clauses.			--
-------------------------------------------------------------------------- 

n = 37::Int

exam1 = [1 .. n]++[1 .. n]

exam2 = list++list 
        where 
        list=[1 .. n]

exam3 = [1 .. n]++[last [1 .. n]]

exam4 = list ++[last list]
        where
        list=[1 .. n]

-------------------------------------------------------------------------- 
--	Factorial using an 'accumulator'.				--
-------------------------------------------------------------------------- 

aFac 0 p = p
aFac n p = aFac (n-1) (p*n)


