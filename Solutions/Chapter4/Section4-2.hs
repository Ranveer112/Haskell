-------------------------------------------------------------------------- 
--	Summing a numerical list.					--
-------------------------------------------------------------------------- 

sumList :: [Int] -> Int

sumList []    = 0  
sumList (a:x) = a + sumList x 


-------------------------------------------------------------------------- 
--	Doubling every element of a list.				--
-------------------------------------------------------------------------- 

double :: [Int] -> [Int]

double []    = []
double (a:x) = (2*a) : double x

